import type { Meta, StoryObj } from '@storybook/react-native-web-vite';
import { useState } from 'react';
import { View } from 'react-native';
import { TextInput } from './TextInput';

const meta: Meta<typeof TextInput> = {
  component: TextInput,
  title: 'Input/TextInput',
  parameters: {
    docs: {
      source: {
        language: 'tsx',
        format: true,
        type: 'code',
      },
    },
  },
  argTypes: {
    label: {
      control: 'text',
      description: 'Floating label text',
    },
    placeholder: {
      control: 'text',
      description: 'Placeholder text when input is empty',
    },
    errorMessage: {
      control: 'text',
      description: 'Error message to display below input',
    },
    editable: {
      control: 'boolean',
      description: 'Whether the input is editable',
    },
    hideClearButton: {
      control: 'boolean',
      description: 'Hide the clear button',
    },
    keyboardType: {
      control: 'select',
      options: ['default', 'email-address', 'numeric', 'phone-pad', 'url'],
      description: 'Keyboard type for input',
    },
  },
};

export default meta;
type Story = StoryObj<typeof TextInput>;

const TextInputStory = (args: TextInputProps & { initialValue?: string }) => {
  const [value, setValue] = useState(args.initialValue ?? '');

  return (
    <View className='flex min-h-96 items-center justify-center p-24'>
      <View className='w-full max-w-320'>
        <TextInput
          {...args}
          value={value}
          onChangeText={setValue}
          onClear={args.onClear}
        />
      </View>
    </View>
  );
};

export const Base: Story = {
  render: (args) => <TextInputStory {...args} />,
  args: {
    label: 'Username',
    editable: true,
    hideClearButton: false,
    keyboardType: 'default',
  },
};

